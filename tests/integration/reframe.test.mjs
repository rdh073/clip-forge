test('cf-reframe --fallback topcrop offsets vertical center upward', { skip: !HAS_FFMPEG, timeout: 30_000 }, () => {
  ensureDemoVideo();
  const r = spawnSync('node', [
    resolve(PLUGIN_ROOT, 'bin/cf-reframe'),
    DEMO_PATH,
    '--output', OUT_PATH,
    '--fallback', 'topcrop',
    '--sample-fps', '4',
  ], { encoding: 'utf-8' });
  assert.equal(r.status, 0, r.stderr);
  const out = JSON.parse(readFileSync(OUT_PATH, 'utf-8'));
  if (out.fallback_used) {
    // topcrop puts cy at ~38% of source_h
    const expectedCy = Math.floor(out.source_h * 0.38);
    assert.equal(out.samples[0].cy, expectedCy, 'topcrop should set cy to upper third');
  }
  try { rmSync(OUT_PATH); } catch {}
});

test('cf-ffmpeg GPU encoder retries with CPU when NVENC rejects the job', { skip: !HAS_FFMPEG, timeout: 30_000 }, () => {
  assert.ok(ensureDemoVideo(), 'demo video should exist or be generated by ffmpeg');
  const cropPath = '/tmp/cf-ffmpeg-gpu-fallback-crop.json';
  const outPath = '/tmp/cf-ffmpeg-gpu-fallback.mp4';
  writeFileSync(cropPath, JSON.stringify({
    version: 2,
    source_w: 1280,
    source_h: 720,
    target_w: 64,
    target_h: 64,
    samples: [{ t_ms: 0, cx: 640, cy: 360, scale: 1 }],
    interp: 'linear',
  }, null, 2));

  const r = spawnSync('node', [
    resolve(PLUGIN_ROOT, 'bin/cf-ffmpeg'), 'reframe-animated',
    '--crop-path', cropPath,
    '--source', DEMO_PATH,
    '--output', outPath,
    '--start-ms', '0',
    '--end-ms', '500',
  ], {
    encoding: 'utf-8',
    cwd: PLUGIN_ROOT,
    env: { ...process.env, CF_FFMPEG_ENCODER: 'gpu' },
  });

  assert.equal(r.status, 0, r.stderr + '\n' + r.stdout);
  assert.match(r.stdout, /"event":"encoder_fallback"/);
  assert.ok(existsSync(outPath), 'fallback render should produce an mp4');
  assert.ok(statSync(outPath).size > 0, 'fallback render should be non-empty');
  try { rmSync(cropPath); rmSync(outPath); } catch {}
});
