// reframe.test.mjs — integration test for bin/cf-reframe end-to-end.
// Generates a 5-second test video on first run via ffmpeg lavfi, then runs
// cf-reframe against it and validates the output schema.
//
// Skips cleanly if ffmpeg isn't available.

import { test } from 'node:test';
import assert from 'node:assert/strict';
import { execSync, spawnSync } from 'node:child_process';
import { existsSync, mkdirSync, readFileSync, writeFileSync, rmSync, statSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const PLUGIN_ROOT = resolve(fileURLToPath(import.meta.url), '../../..');
const DEMO_PATH = resolve(PLUGIN_ROOT, 'samples/demo-5s.mp4');
const OUT_PATH = resolve(PLUGIN_ROOT, '/tmp/cf-reframe-itest.json');

function which(cmd) {
  try {
    return execSync('command -v ' + cmd, { stdio: ['ignore', 'pipe', 'ignore'] }).toString().trim();
  } catch { return null; }
}

const HAS_FFMPEG = !!which('ffmpeg');

function ensureDemoVideo() {
  if (existsSync(DEMO_PATH)) return true;
  if (!HAS_FFMPEG) return false;
  mkdirSync(dirname(DEMO_PATH), { recursive: true });
  // 5 seconds, 1280x720, 30fps, with a moving rectangle to simulate motion.
  // testsrc produces visible content; MediaPipe won't detect a face here,
  // so the test exercises the FALLBACK path — which is exactly what we want
  // to verify (graceful degradation is the contract).
  const r = spawnSync('ffmpeg', [
    '-y', '-hide_banner', '-loglevel', 'error',
    '-f', 'lavfi', '-i', 'testsrc=duration=5:size=1280x720:rate=30',
    '-c:v', 'libx264', '-preset', 'ultrafast', '-crf', '28',
    '-pix_fmt', 'yuv420p',
    DEMO_PATH,
  ]);
  return r.status === 0;
}

test('cf-reframe end-to-end on demo video (skipped when ffmpeg missing)', { skip: !HAS_FFMPEG, timeout: 30_000 }, async () => {
  const ok = ensureDemoVideo();
  assert.ok(ok, 'demo video should exist or be generated by ffmpeg');

  // Run cf-reframe synchronously; record wall-clock.
  const t0 = Date.now();
  const r = spawnSync('node', [
    resolve(PLUGIN_ROOT, 'bin/cf-reframe'),
    DEMO_PATH,
    '--output', OUT_PATH,
    '--sample-fps', '4',
  ], { encoding: 'utf-8' });
  const elapsed = Date.now() - t0;

  assert.equal(r.status, 0, 'cf-reframe should exit 0 even when no faces found\n' + r.stderr);
  assert.ok(elapsed < 20_000, 'should complete in <20s wall clock (got ' + elapsed + 'ms)');

  // Parse output
  const out = JSON.parse(readFileSync(OUT_PATH, 'utf-8'));

  // Schema validation — must remain compatible with cf-ffmpeg.
  // v0.4.0 pillar 6 bumped cf-reframe output to v3 (additive: v2 readers
  // ignore unknown fields; v3 readers accept v2 inputs).
  assert.equal(out.version, 3, 'crop_path.json should be version 3 (v0.4.0 pillar 6)');
  assert.equal(out.source_w, 1280);
  assert.equal(out.source_h, 720);
  assert.equal(out.target_w, 1080);
  assert.equal(out.target_h, 1920);
  assert.ok(Array.isArray(out.samples), 'samples must be an array');
  assert.ok(out.samples.length > 0, 'samples must be non-empty');

  // Every sample must have the v1-compatible fields cf-ffmpeg reads.
  for (const s of out.samples) {
    assert.equal(typeof s.t_ms,  'number');
    assert.equal(typeof s.cx,    'number');
    assert.equal(typeof s.cy,    'number');
    assert.equal(typeof s.scale, 'number');
    assert.ok(s.cx >= 0 && s.cx <= out.source_w, 'cx must be within source bounds');
    assert.ok(s.cy >= 0 && s.cy <= out.source_h, 'cy must be within source bounds');
  }

  // testsrc has no faces, so the no-face fallback kicks in. The exact
  // reason depends on whether the model file is installed in this run:
  //   • model present → low_face_yield (detector ran, found nothing)
  //   • model absent  → model_missing  (detector never initialized)
  // Either is a valid fallback; assert it triggered and metadata is honest.
  assert.equal(out.fallback_used, true, 'testsrc has no faces → fallback should trigger');
  assert.equal(out.detector, 'center-fallback', 'detector field should record the fallback variant');
  assert.ok(typeof out.fallback_reason === 'string' && out.fallback_reason.length > 0,
    'fallback_reason should be populated; got: ' + out.fallback_reason);

  // Cleanup
  try { rmSync(OUT_PATH); } catch {}
});

test('fallback metadata is populated when no faces are present', { skip: !HAS_FFMPEG, timeout: 30_000 }, () => {
  ensureDemoVideo();
  const r = spawnSync('node', [
    resolve(PLUGIN_ROOT, 'bin/cf-reframe'),
    DEMO_PATH,
    '--output', OUT_PATH,
    '--sample-fps', '4',
  ], { encoding: 'utf-8' });
  assert.equal(r.status, 0, r.stderr);
  const out = JSON.parse(readFileSync(OUT_PATH, 'utf-8'));
  // Reality check: fallback should trigger and reason should be informative.
  // The Phase 2E success-path test (added separately with a face fixture)
  // exercises the non-fallback success path.
  assert.equal(out.fallback_used, true);
  assert.ok(out.fallback_reason && out.fallback_reason.length > 0);
  assert.match(out.fallback_reason, /(low_face_yield|model_missing|detector_unavailable)/,
    'fallback_reason should classify the cause; got: ' + out.fallback_reason);
  try { rmSync(OUT_PATH); } catch {}
});

