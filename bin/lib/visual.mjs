// visual.mjs — image-generation dispatcher (v0.4.0 pillar 5).
//
// Provider resolution (PLAN-v0.4.0 §3.5):
//   CF_VISUAL_PROVIDER=<name>     → explicit override (fal | nanobanana | replicate)
//   FAL_API_KEY            set    → fal Flux Schnell (~$0.003/img, cheapest)
//   GEMINI_API_KEY         set    → Nano Banana (~$0.04/img, brand-consistent)
//   REPLICATE_API_TOKEN    set    → Replicate (varies)
//   none                          → { fallback_used: true, fallback_reason: 'no_visual_provider' }
//
// Mock injection: CF_VISUAL_MOCK=<path> bypasses every network adapter.
// Mirrors CF_TTS_MOCK / CF_LLM_MOCK behavior — script reads brief JSON on
// stdin, writes generated bytes to req.paths[], emits result JSON on stdout.
//
// SoC: adapters under bin/lib/visual/* own provider I/O. This file owns
// precedence resolution, mock injection, and the unified return shape.

import { spawnSync } from 'node:child_process';
import { existsSync } from 'node:fs';

import * as Fal         from './visual/fal.mjs';
import * as NanoBanana  from './visual/nanobanana.mjs';
import * as Replicate   from './visual/replicate.mjs';

const PROVIDERS = { fal: Fal, nanobanana: NanoBanana, replicate: Replicate };
const PRECEDENCE = ['fal', 'nanobanana', 'replicate'];

export function resolveProvider(explicit) {
  const override = explicit || process.env.CF_VISUAL_PROVIDER || '';
  if (override) {
    if (!PROVIDERS[override]) {
      throw new Error('visual: unknown provider in CF_VISUAL_PROVIDER/explicit: ' + override);
    }
    return { name: override, adapter: PROVIDERS[override] };
  }
  for (const name of PRECEDENCE) {
    if (PROVIDERS[name].available()) {
      return { name, adapter: PROVIDERS[name] };
    }
  }
  return null;
}

function runMock(mockPath, brief) {
  if (!existsSync(mockPath)) {
    return { fallback_used: true, fallback_reason: 'visual_mock_missing', cost_usd: 0 };
  }
  const r = spawnSync(process.execPath, [mockPath], {
    input:     JSON.stringify(brief),
    encoding:  'utf-8',
    maxBuffer: 32 * 1024 * 1024,
  });
  if (r.status !== 0) {
    return { fallback_used: true, fallback_reason: 'visual_mock_exit_nonzero',
             detail: (r.stderr || '').slice(-240), cost_usd: 0 };
  }
  const trimmed = (r.stdout || '').trim();
  if (!trimmed) {
    return { fallback_used: true, fallback_reason: 'visual_mock_empty_output', cost_usd: 0 };
  }
  try { return JSON.parse(trimmed); }
  catch (e) {
    return { fallback_used: true, fallback_reason: 'visual_mock_invalid_json',
             detail: e.message, cost_usd: 0 };
  }
}

/**
 * Generate one or more images via the resolved provider.
 *
 * @param {{prompt:string, paths:string[], aspect?:string, count?:number,
 *          style_ref_path?:string, brand_kit?:object, seed?:number,
 *          provider?:string}} req
 * @returns {Promise<{paths:string[], cost_usd:number, provider_used:string,
 *                     prompt_used:string} | {fallback_used:true, fallback_reason:string}>}
 */
export async function generate(req) {
  if (!req || typeof req !== 'object') {
    throw new Error('visual.generate: req must be an object');
  }
  if (!Array.isArray(req.paths) || req.paths.length === 0) {
    throw new Error('visual.generate: req.paths must be a non-empty array');
  }
  const mockPath = process.env.CF_VISUAL_MOCK || '';
  if (mockPath) {
    const out = runMock(mockPath, req);
    if (out.fallback_used) return { ...out, provider_used: 'mock' };
    return {
      paths:         Array.isArray(out.paths) ? out.paths : req.paths.slice(0, 1),
      cost_usd:      typeof out.cost_usd === 'number' ? out.cost_usd : 0,
      provider_used: 'mock',
      prompt_used:   String(out.prompt_used || req.prompt || ''),
      model:         out.model || 'mock',
    };
  }
  const chosen = resolveProvider(req.provider);
  if (!chosen) {
    return { fallback_used: true, fallback_reason: 'no_visual_provider', cost_usd: 0,
             provider_used: null };
  }
  let result;
  try {
    result = await chosen.adapter.generate(req);
  } catch (e) {
    return { fallback_used: true, fallback_reason: chosen.name + '_throw',
             detail: e.message, cost_usd: 0, provider_used: chosen.name };
  }
  return { ...result, provider_used: chosen.name };
}

export { PROVIDERS, PRECEDENCE };
